USE radiske;

DELIMITER $$
CREATE TRIGGER SALVAR_LOG_PERMISSAO
AFTER UPDATE 
ON radiske.PERMISSAO
FOR EACH ROW
BEGIN
	SET @id := (SELECT ID_SESSAO FROM SESSAO ORDER BY ID_SESSAO DESC LIMIT 1);
	INSERT INTO LOG (ID_SESSAO, DADOS) VALUES (@id, CONCAT('{"ID_PERMISSAO": ' + OLD.ID_PERMISSAO + ', "NOME": "' + OLD.NOME + '", "MOMENTO": ' + OLD.MOMENTO + '}'));
END;
$$