USE appamf;

DELIMITER $$
CREATE TRIGGER SALVAR_LOG_CURSO
AFTER UPDATE 
ON appamf.curso
FOR EACH ROW
BEGIN
	SELECT @id := ID_SESSAO FROM SESSAO ORDER BY ID_SESSAO DESC LIMIT 1;
	INSERT INTO LOG (ID_SESSAO, DADOS) VALUES (@id, '{"ID_CURSO": ' + OLD.ID_CURSO + ', "NOME": "' + OLD.NOME + '", "LOGO": "' + OLD.LOGO + '", "EXCLUIDO": ' + OLD.EXCLUIDO + ', "MOMENTO": ' + OLD.MOMENTO + ', "ID_SESSAO": ' + OLD.ID_SESSAO + '}');
END;
$$